            <h1>5 routines pour bien commencer la journée </h1>
            <p class="blog-date">Publié le 12 mars 2025</p>
            <img src="../assets/img/blog1.jpg" alt="Routine du matin" style="width: 100%; border-radius: 12px; margin: 2rem 0;" />

            <p>La façon dont tu commences ta journée peut influencer tout le reste. Une bonne routine matinale ne sert pas seulement à te réveiller, elle te donne un cap, de l’énergie, et un sentiment de contrôle. Voici 5 routines simples mais puissantes pour démarrer ta journée du bon pied.</p>

            <h2>1. Se lever à une heure fixe <i class="fas fa-clock"></i></h2>
            <p>Tu n’as pas besoin de te lever à 5h du matin pour être productif. Ce qui compte, c’est la régularité. En te levant chaque jour à la même heure, tu aides ton cerveau à se réveiller naturellement, sans stress. Cela crée une stabilité qui favorise la clarté mentale.<br><strong>Astuce :</strong> Choisis une heure réaliste selon ton rythme de vie, et tiens-toi-y, même le week-end.</p>

            <h2>2. Éviter le téléphone au réveil <i class="fas fa-ban"></i></h2>
            <p>C’est tentant de consulter ses notifications dès les premières secondes… mais c’est aussi le moyen le plus rapide de polluer ton esprit. Ton attention est précieuse. Garde-la pour toi au réveil.<br><strong>Astuce :</strong> Remplace ton scroll matinal par quelques respirations profondes ou un moment calme sans écran.</p>

            <h2>3. Bouger son corps dès le matin <i class="fas fa-running"></i></h2>
            <p>Pas besoin d’un entraînement intense. Quelques étirements, 5 minutes de marche ou une petite routine de mobilité peuvent réveiller ton corps et activer ton énergie.<br><strong>Astuce :</strong> Prépare ta tenue de la veille pour ne pas réfléchir au saut du lit. Le plus dur, c’est de commencer !</p>

            <h2>4. Planifier sa journée en 5 minutes <i class="fas fa-calendar-alt"></i></h2>
            <p>Rien de plus rassurant que de savoir où tu vas. Prends 5 minutes pour noter ce que tu dois vraiment faire aujourd’hui. Une bonne planification réduit la charge mentale et augmente ta concentration.<br><strong>Astuce :</strong> Note 3 priorités maximum. Pas plus. Mieux vaut en faire peu mais bien.</p>

            <h2>5. Nourrir son esprit (et son corps) <i class="fas fa-mug-hot"></i></h2>
            <p>Commence ta journée avec un petit moment pour toi : lire, écrire, méditer ou simplement savourer ton petit-déjeuner en silence. Ça te centre et te donne le ton de la journée.<br><strong>Astuce :</strong> Choisis une activité calme que tu aimes vraiment. Ce moment t’appartient.</p>

